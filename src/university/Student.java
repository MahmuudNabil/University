    private void insert() throws SQLException {
        String gender=male.getText();
        if(female.isSelected())
            gender=female.getText();
        String miss ="لقد نسيت ادخال البيانات التالية"+"\n";
        if(id.getText().equals(""))
            miss+="رقم الطالب"+"\n";
        if(name.getText().equals(""))
            miss+="إسم الطالب"+"\n";
        if(mobile.getText().equals(""))
            miss+="رقم الموبايل"+"\n";
        if(!male.isSelected() && !female.isSelected())
             miss+="الجنس"+"\n";
        if(!id.getText().equals("") && !name.getText().equals("") && !mobile.getText().equals("") && (male.isSelected() || female.isSelected()))
        {
            conn=ConnectionDataBase.connect();
            try {
                ps=conn.prepareStatement("select * from student where id ='"+Integer.parseInt(id.getText())+"'");
                rst=ps.executeQuery();
                if(rst.next())
                    JOptionPane.showMessageDialog(null, "رقم الطالب موجود مسبقا ","موجود",2);
                else
                {
                    ps=conn.prepareStatement("insert into student values('"+Integer.parseInt(id.getText())+"','"+name.getText()+
                            "','123','"+mobile.getText()+"','"+gender+"','0','0','0','0')");
                     ps.execute();
                    JOptionPane.showMessageDialog(null, "تمت اضافة الطالب بنجاح");
                    clear();
                    
                }
            } catch (SQLException ex) {
                Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                conn.close();
            }
        }
        else
            JOptionPane.showMessageDialog(null, miss, "فقد بيانات الدخول",2);
            
    }

    private void update() {
        String miss ="لقد نسيت ادخال البيانات التالية"+"\n";
        String gender=male.getText();
        if(female.isSelected())
            gender=female.getText();
        if(id.getText().equals(""))
            miss+="رقم الطالب"+"\n";
         if(!id.getText().equals("") )
         {
             try {
                    conn=ConnectionDataBase.connect();
                    ps=conn.prepareStatement("update student set name= '"+name.getText()+"',mobile='"+mobile.getText()+"', gender='"+gender+"'where id = '"+Integer.parseInt(id.getText())+"'");
                    ps.execute();
                    JOptionPane.showMessageDialog(null, "تم تعديل الطالب بنجاح","تعديل الطالب",JOptionPane.INFORMATION_MESSAGE);
                    clear();
                    id.setEnabled(true);
                    name.setEditable(false);
                    mobile.setEnabled(false);
                    male.setEnabled(false);
                    female.setEnabled(false);
                 } catch (SQLException ex) {
                        Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
                 }finally{
                            try {
                                conn.close();
                            } catch (SQLException ex) {
                                Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
                            }
                 }
         }
        else
            JOptionPane.showMessageDialog(null, miss, "فقد بيانات الدخول",2);
       
       
    }

    private void delete() {
       String miss ="لقد نسيت ادخال البيانات التالية"+"\n";
        if(id.getText().equals(""))
            miss+="رقم الطالب"+"\n";
         if(!id.getText().equals("") )
         {  
           try {
                    conn=ConnectionDataBase.connect();
                    ps=conn.prepareStatement("delete from student where id ='"+Integer.parseInt(id.getText())+"'");
                    ps.execute();
                    JOptionPane.showMessageDialog(null, "تم حذف الطالب بنجاح","حذف الطالب",JOptionPane.INFORMATION_MESSAGE);
                    clear();
                    id.setText("");
              } catch (SQLException ex) {
                      Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
           }finally{
               try {
                   conn.close();
               } catch (SQLException ex) {
                   Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
         }
         
        else
            JOptionPane.showMessageDialog(null, miss, "فقد بيانات الدخول",2);
    }

    private void clear() {
       id.setText("");
       name.setText("");
       mobile.setText("");
       
       male.setSelected(false);
       female.setSelected(false);
    }
