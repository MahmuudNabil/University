    private void idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyPressed
        if(evt.getKeyCode()==evt.VK_ENTER)
        {
            if(id.getText().equals(""))
                JOptionPane.showMessageDialog(null, "رقم التعريف فارغ");
            else
            {
              conn =ConnectionDataBase.connect();
                try {
                    ps=conn.prepareStatement("select * from student where id = '"+Integer.parseInt(id.getText())+"'");
                    rst=ps.executeQuery();
                    if(rst.next())
                    {
                        id.setEnabled(false);
                        
                        name.setText(rst.getString(2));
                        name.setEnabled(true);
                        
                        mobile.setText(rst.getString(4));
                        mobile.setEnabled(true);
                        
                        String gender =rst.getString(5);
                        if(gender.equals(male.getText()))
                            male.setSelected(true);
                        else 
                            female.setSelected(true);
                        male.setEnabled(true);
                        female.setEnabled(true);
                        
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
                }finally{
                  try {
                      conn.close();
                  } catch (SQLException ex) {
                      
                  }
                }
            }
        }
    }//GEN-LAST:event_idKeyPressed

 
